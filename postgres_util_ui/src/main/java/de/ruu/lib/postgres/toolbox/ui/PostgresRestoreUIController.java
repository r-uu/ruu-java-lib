	/**
	 * Validates all input fields before executing restore.
	 * Shows error dialog if validation fails.
	 * @param executable path to pg_restore executable
	 * @param sourceFile path to source backup file
	 * @param host database host
	 * @param port database port
	 * @param database database name
	 * @param username database username
	 * @return true if all inputs are valid, false otherwise
	 */
	private boolean validateInputs(String executable, String sourceFile, String host, String port, String database, String username)
	{
		// Check if executable path is set and valid
		if (executable == null || executable.trim().isEmpty() || executable.equals("."))
		{
			AlertDialog.showAndWait(
				"Invalid Configuration",
				"Please select a valid pg_restore executable.",
				"Current value: '" + executable + "' is not a valid executable path.\n\n" +
				"Click the folder icon next to 'Executable' to select pg_restore or pg_restore.exe",
				Alert.AlertType.ERROR
			);
			return false;
		}

		File executableFile = new File(executable);
		if (!executableFile.exists())
		{
			AlertDialog.showAndWait(
				"Executable Not Found",
				"The pg_restore executable does not exist.",
				"Path: " + executable + "\n\n" +
				"Please verify the path and select a valid pg_restore executable.",
				Alert.AlertType.ERROR
			);
			return false;
		}

		if (!executableFile.canExecute())
		{
			AlertDialog.showAndWait(
				"No Execute Permission",
				"The selected file is not executable.",
				"Path: " + executable + "\n\n" +
				"Please select the pg_restore executable file (not a directory).",
				Alert.AlertType.ERROR
			);
			return false;
		}

		// Check if source file path is set and valid
		if (sourceFile == null || sourceFile.trim().isEmpty() || sourceFile.equals("."))
		{
			AlertDialog.showAndWait(
				"Invalid Source File",
				"Please select a valid backup file to restore.",
				"Current value: '" + sourceFile + "' is not a valid file path.\n\n" +
				"Click the folder icon next to 'Source File' to select a backup file.",
				Alert.AlertType.ERROR
			);
			return false;
		}

		File source = new File(sourceFile);
		if (!source.exists())
		{
			AlertDialog.showAndWait(
				"Source File Not Found",
				"The backup file does not exist.",
				"Path: " + sourceFile + "\n\n" +
				"Please select an existing backup file to restore.",
				Alert.AlertType.ERROR
			);
			return false;
		}

		if (!source.isFile())
		{
			AlertDialog.showAndWait(
				"Invalid Source File",
				"The selected source is not a file.",
				"Path: " + sourceFile + "\n\n" +
				"Please select a valid backup file (not a directory).",
				Alert.AlertType.ERROR
			);
			return false;
		}

		// Check required database connection parameters
		if (host == null || host.trim().isEmpty())
		{
			AlertDialog.showAndWait(
				"Missing Host",
				"",
				"Please enter the PostgreSQL host address.",
				Alert.AlertType.ERROR
			);
			return false;
		}

		if (database == null || database.trim().isEmpty())
		{
			AlertDialog.showAndWait(
				"Missing Database Name",
				"",
				"Please enter the database name to restore to.",
				Alert.AlertType.ERROR
			);
			return false;
		}

		if (username == null || username.trim().isEmpty())
		{
			AlertDialog.showAndWait(
				"Missing Username",
				"",
				"Please enter the PostgreSQL username.",
				Alert.AlertType.ERROR
			);
			return false;
		}

		// Validate port number
		try
		{
			int portNum = Integer.parseInt(port);
			if (portNum < 1 || portNum > 65535)
			{
				AlertDialog.showAndWait(
					"Invalid Port",
					"Port must be between 1 and 65535.",
					"Current value: " + port,
					Alert.AlertType.ERROR
				);
				return false;
			}
		}
		catch (NumberFormatException e)
		{
			AlertDialog.showAndWait(
				"Invalid Port",
				"Port must be a valid number.",
				"Current value: " + port,
				Alert.AlertType.ERROR
			);
			return false;
		}

		return true;
	}

