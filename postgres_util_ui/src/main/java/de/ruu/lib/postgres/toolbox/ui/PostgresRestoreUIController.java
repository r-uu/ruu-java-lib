		if (not(validateInputs(executable, sourceFile, host, port, database, username)))
		{
			return;
		}

		try
		{
			PostgresToolBox
					.restore
					(
							Path.of(executable),
							host,
							Integer.parseInt(port),
							database,
							username,
							password,
							Path.of(sourceFile)
					);
			AlertDialog
					.showAndWait
						(
							"success",
							"restore complete",
							"database: " + database,
							Alert.AlertType.INFORMATION
						);
		}
		catch (IOException | InterruptedException e)
		{
			ExceptionDialog.showAndWait("failure executing restore", e);
		}
